/* 题目：水仙化树
 * 打印出所有的“水仙花数”，所谓“水仙花数”是指一个三位数，其各位数字立方和等于该数本身。
 * 例如：153是一个“水仙花数”，因为153 = 1的三次方＋5的三次方＋3的三次方。
 */
#include "stdio.h"
int main()
{
    int g, s, b;

    for (int i = 100; i <= 999; i++)
    {
        g = i % 10;
        s = i / 10 % 10;
        b = i / 100 % 10;
        if (i == g * g * g + s * s * s + b * b * b)
        {
            printf("%d%d%d\n", b, s, g);
        }
    }
    return 0;
}
